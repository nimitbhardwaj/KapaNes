#include "../include/test_memory_unit.hpp"


void MemoryUnitTest::testGetByteAtRam() {
    mem->setByteAt(0x0602, 0x33);
    mem->setByteAt(0x0603, mem->getByteAt(0x0602)+1);
    CPPUNIT_ASSERT(mem->getByteAt(0x0603) == 0x34);
}

void MemoryUnitTest::testSetByteAtRam() {
    mem->setByteAt(0x0602, 0x33);
    mem->setByteAt(0x0603, mem->getByteAt(0x0602)+1);
    CPPUNIT_ASSERT(mem->getByteAt(0x0603) == 0x34);
}
